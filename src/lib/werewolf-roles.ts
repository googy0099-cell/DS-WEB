export function getTeam(role: string): "wolf" | "village" | "indy" | "vampire" {
  if (wolfRoles.includes(role)) return "wolf";
  if (indyRoles.includes(role)) return "indy";
  if (vampireRoles.includes(role)) return "vampire";
  return "village";
}

